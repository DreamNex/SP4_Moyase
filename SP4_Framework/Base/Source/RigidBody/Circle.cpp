#include "Circle.h"

/*************************************************************************************************************
	CONSTRUCTOR(S) && DESTRUCTOR
*************************************************************************************************************/
Circle::Circle(Vector2 &origin, float radius) : CollisionComponent(origin)
{
	this->radius = radius;
}

Circle::Circle() :
radius(0)
{
}

Circle::~Circle()
{
}


/*************************************************************************************************************
	ACCESOR(S)
*************************************************************************************************************/
float Circle::GetRadius(void)const{ return this->radius; }


/*************************************************************************************************************
	MUTATOR(S)
*************************************************************************************************************/
void Circle::SetRadius(float radius){ this->radius = radius; }